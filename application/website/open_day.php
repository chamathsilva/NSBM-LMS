'Global Graduate – NSBM Open Day” the annual higher education exhibition organized by the National School of Business Management (NSBM) and its partner universities across the globe will be held on 18th to 20th September 2015 at NSBM premises, Nugegoda. This will be a great opportunity to school leavers who has recently finished their A/Ls to plan their higher education path and also to the parents as “Global Graduate” provide them a unique platform to meet the best in the higher education industry.<br><br>

Offering many opportunities and whole new level of future experience, “Global Graduate” will accommodate all the knowledge seekers visiting the exhibition from across the country. The unique features of the “Global Graduate” is that apart featuring higher education options the demonstration of latest technology used in the higher education sector and the world’s best universities who are practicing , bringing them to a single platform, offering Sri Lankan youth to experience the future of higher education at their fingertips.<br><br>

The exhibition will consist of dedicated zones such as Technology Zone, Cross-Boarders Zone, Creativity Zone, Information Zone, and Food & Beverage Zone etc. A demonstration of 21st century university amenities with features such as E- teacher, interactive whiteboards, interactive multimedia projectors, automated attendance systems, library systems, interior designing exhibitions, short film festival, creative multimedia studio workshops, foreign internship programmes together with student loan facilities will be ready to take the whole NSBM premises to a next level of futuristic higher education on the 18th, 19th & 20th September, 2015 throughout the day. The exhibition will take place at National School of Business Management, 309, High Level Road, Nugegoda (Near Anula Vidyalaya).
