<h2 align="center"><font color="#80002a">Walk for a cause - NSBM Walk 2014</font></h2>
<center><img src="../../assets/images/walk.jpeg" width="70%" height="70%"></center>
<br>
<div class="container-fluid">
    <font size="4"><p align="justify">Being human is a choice, bearing that in mind the undergraduates of 13.2 ad 14.1 BSc. in Business Management (special) students organized the first ever walk and carnival for the National School of Business Management which took place on 14th of December 2014 at the Independence Square and CR and FC grounds from 2.00pm onwards under the theme ‘being human’. The front runners of the walk was the Dr. E.A Weerasinghe and Mr. D.M.A Kulasooriya. The sole purpose of this endeavour was to raise money for the children’s ward at the Maharagama cancer hospital.

            To make this more vibrant the organizers arranged a competition for all the batches to define how they interpret humanity. There were numerous contenders who cameunder vivid themes like social media and human life, say no to harassment, happiness, stop animal cruelty, Sri Lankan cricket, smoking kills, 5 human values and many more. Winners were the 14.2 Plymouth computing batch who came up with abreadth taking techno truck with the theme social media and human life. There was a vehicle parade which followed the walk simultaneously.

            There were several mascots like Donald duck, Barney, Super Mario, and Garfield to entertain the crowd. Also there were several bike stunts performed on the way which entertained the students and the on lookers. Even though the carnival was mildly interrupted by the rain the students stayed on for the musical show and the game stalls that were organized.

            The carnival was coloured by the famous artists Billy Fernando and 242, Randir, LahiruPerera, Gayan Arosha, Tehan and Shameen. The platinum sponsor for this event was National Savings Bank and the Silver sponsor was MAGA engineering Pvt. Ltd.</p>
