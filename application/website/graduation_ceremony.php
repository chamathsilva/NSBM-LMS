  <h1><font color="#80002a">Plymouth University - Graduation Ceremony 2015</font></h1>
  <blockquote>
  <p align="justify">The inception Graduation Ceremony of the Plymouth University, United Kingdom for the degree programmes offered in collaboration with National School of Business Management (NSBM) was held on 27 November 2015 at BMICH under the distinguished patronage of Hon. Mahinda Samarasinghe - Minister of Skills Development & Vocational Training.<br><br>
 
One hundred and three students were graduated in this colourful event in obtaining degrees in BSc (Honours) Marketing Management, BSc (Honours) Accounting and Finance, BSc (Honours) Operations and Logistics Management, BSc (Honours) International Management and Business, BSc (Honours) Events, Tourism and Hospitality Management, BSc (Honours) Business Communication. Miss. Raneesha Hansali Peiries was awarded the ‘Plymouth University Academic Partnership Prize’ and the awarded for the ‘Overall Best Performance – Management Programmes of NSBM’ and Mr. T D Waruna Hemakumara was also honored as the ‘Overall Best Performer of NSBM – Computing Programmes’ and awarded with the Highest Achiever award in Computing.<br><br>

Remarking the historical day of NSBM graduation history, Mr. Weththinge Jinadasa was awarded with an Honorary Degree of Honorary Doctor in Business from the Plymouth University, UK for his achievements the service rendered towards empowering Sri Lankan business sector.<br><br>

Mr. P Ranepura, Secretary of the Ministry of Skills Development and Vocational Training, Prof. David Coslett - Vice Chancellor of Plymouth University, Prof. Simon Payne – Deputy Vice Chancellor, Plymouth University, Dr. E A Weerasinghe – Vice Chancellor/ CEO, NSBM; Dr. D M A Kulasooriya – Director Academic Affairs, NSBM; Dr. Damith Mudugamuwa – Dean School of Computing, NSBM, the distinguished team of Academia from Plymouth University and NSBM, Heads of Departments and Professionals were among the special guests of the event.<br><br>

Plymouth University is one of the UK's most prominent and dynamic universities with an educational history dating back to 1862. Consistently ranked as one of the top three modern universities, Plymouth University has, over 30,000 students with almost 3,000 staff and are well known for their high academic standards.<br><br>

The University has a long record of high quality innovative research as demonstrated by its achievements in the RAE. With its strong affiliation with Plymouth University, NSBM offers a wide array of degrees for participants in disciplines of Management and Computing with a current undergraduate population exceeding one thousand.<br><br>

As the degree awarding organisation of NIBM, NSBM is established under the Ministry of Skills Development and Vocational Training, currently functioning under the patronage of the Hon. Mahinda Samarasinghe. This link with the government of Sri Lanka guarantees not only the stability of NSBM as an educational establishment but also the quality of education. As a higher education services provider associated with the government of Sri Lanka, NSBM’s primary objective is the welfare and the development of Sri Lanka’s future generations through the provision of the highest international standard of education.  This makes us unique from the plethora of private education service providers in the country. 
